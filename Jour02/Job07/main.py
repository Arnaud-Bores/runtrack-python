def quel_developpeur(langage):
    if langage == "javascript":
        print("tu es un developpeur web")
    elif langage == "python":
        print("tu es un developpeur IA")
    elif langage == "java":
        print("tu es un developpeur logiciel")
    elif langage == "reactjs":
        print("tu es un developpeur mobile")
    else:
        print("un jour, je serai le meilleur développeur...")

# Exemples d'utilisation
quel_developpeur("javascript")  # Affiche "tu es un développeur web"
quel_developpeur("python")  # Affiche "tu es un développeur IA"
quel_developpeur("java")  # Affiche "tu es un développeur logiciel"
quel_developpeur("reactjs")  # Affiche "tu es un développeur mobile"
quel_developpeur("ruby")  # Affiche "un jour, je serai le meilleur développeur..."
